#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*str;
	unsigned char	ch;

	ch = c;
	str = (void*)s;
	while (n--)
	{
		if (*str == ch)
			return (str);
		if (n)
			str++;
	}
	return (NULL);
}
