int	ft_nbrlen(int n)
{
	int		len;

	len = 0;
	if (n == 0)
	{
		return (1);
	}
	else if (n < 0)
	{
		++len;
	}
	while (n != 0)
	{
		n /= 10;
		++len;
	}
	return (len);
}
